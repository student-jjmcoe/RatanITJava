class Relational_Operators
     {
          public static void main(String[]args)
             {
                 int a = 20, b = 5;
                 System.out.println(a>b);
                 System.out.println(a<b);
                 System.out.println(a>=b);
                 System.out.println(a<=b);
                 System.out.println(a==b);
                 System.out.println(a!=b);
             }
     }
                 