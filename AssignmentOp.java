class AssignmentOp
    {
         public static void main(String[]args)
           {
                int val = 10;
                System.out.println(val);

                val=val+10;
                System.out.println(val);

                int data = 10;
                data+=10;
                System.out.println(data);

                int num = 10;
                num-=4;
                System.out.println(num);

                int a = 10;
                a+=1;
                a-=2;
                a*=3;
                a/=3;
                a%=3;
                 System.out.println(a);
           }
    }