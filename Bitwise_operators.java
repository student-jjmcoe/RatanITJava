class Bitwise_operators
       {
           public static void main(String[]args)
                {
                      System.out.println(7&10);
                      System.out.println(4&6);
                      System.out.println(2&4);

                      System.out.println(10|7);
                      System.out.println(4|6);
                      System.out.println(2|4);

                      System.out.println(~123);
                      System.out.println(~-123);
                }
      }