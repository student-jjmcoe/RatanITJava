class Bitwise_LogicalCal
     {
           public static void main(String[]args)
                {
                     //Bitwise Cal
                     System.out.println(10 & 6);
                     System.out.println(12 | 11);

                     //Logical Calculation
                     System.out.println(10>20 & 30<40);
                     System.out.println(10<20 | 30<40);

                    //Logical Operator
                    System.out.println(10>20 && 20<30);
                    System.out.println(10>20 || 20<30);
                }
     }