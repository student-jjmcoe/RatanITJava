class EscapeSeq
    {
         public static void main(String[]args)
             {
                  System.out.println("I am Shweta");
                
                  System.out.println("I \"am\" Shweta");
                  System.out.println("I \'am\' Shweta");
                  System.out.println("I \\am\\ Shweta");

                  System.out.println("I\t\tam\t\tShweta");
                  System.out.println("I\nam\nShweta");
             }
    }